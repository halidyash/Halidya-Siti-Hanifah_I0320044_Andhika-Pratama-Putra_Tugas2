#Luas Persegi Panjang
print("--Luas Persegi Panjang dalam cm--")
panjang = float(input("Masukkan Panjang persegi panjang : "))
lebar = float(input("Masukkan Lebar persegi panjang : "))
luas_persegi_panjang = panjang * lebar
print("Luas Persegi Panjang adalah ",luas_persegi_panjang, "cm2")

#Luas Lingkaran
print("--Luas Lingkaran dalam cm--")
jari_jari = float(input("Masukkan jari-jari lingkaran : "))
luas_lingkaran = ((22/7) * jari_jari * jari_jari)
print("Luas Lingkaran adalah ",luas_lingkaran, "cm2")

#Luas Kubus
print("--Luas Kubus dalam cm--")
sisi = float(input("Masukkan sisi kubus : "))
luas_kubus = (6 * (sisi * sisi))
print("Luas Kubus adalah",luas_kubus, "cm2")

#Konversi Suhu dari Celcius ke Fahrenheit
print("--Konversi Suhu dari Celcius ke Fahrenheit--")
celcius = float(input("Masukkan suhu dalam celcius : "))
fahrenheit = ((9/5) * celcius + 32)
print("Suhu dalam Fahrenheit adalah ",fahrenheit,"F")

#Konversi Suhu dari Reamur ke Kelvin
print("--Konversi Suhu dari Reamur ke Kelvin--")
Reamur = float(input("Masukkan suhu dalam reamur : "))
Kelvin = ((5/4) * Reamur + 273)
print("Suhu dalam Kelvin adalah ",Kelvin,"K")







